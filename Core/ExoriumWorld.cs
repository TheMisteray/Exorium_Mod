        private void ShadowHouse()
        {
            int attempts = 0;
            bool success = false;
            int i = 0;
            int j = 0;
            while (!success)
            {
                if (attempts > 10000)
                    return;
                i = Main.maxTilesX / 2 + (WorldGen.genRand.Next(-Main.maxTilesX / 6, Main.maxTilesX / 6));
                j = (Main.maxTilesY / 5) * 2 + (WorldGen.genRand.Next(-Main.maxTilesY / 12, Main.maxTilesY / 6));
                //Check for other protected structures
                if (WorldGen.structures.CanPlace(new Rectangle(i, j, _shadowhouseShape.GetLength(1), _shadowhouseShape.GetLength(0)), 50))
                    success = true;
            }
            for (int y = 0; y < _shadowhouseShape.GetLength(0); y++)
            {
                for (int x = 0; x < _shadowhouseShape.GetLength(1); x++)
                {
                    int k = i + x;
                    int l = j + y;
                    Tile tile = Framing.GetTileSafely(k, l);
                    switch (_shadowhouseShape[y, x])
                    {
                        case 0:
                            tile.active(false);
                            break;
                        case 1:
                            WorldGen.KillTile(k, l);
                            WorldGen.PlaceTile(k, l, TileType<DarkBrick>());
                            tile.slope(0);
                            break;
                        case 2:
                            WorldGen.KillTile(k, l);
                            WorldGen.PlaceTile(k, l, TileType<DarkBrick>());
                            tile.slope(2);
                            break;
                        case 3:
                            WorldGen.KillTile(k, l);
                            WorldGen.PlaceTile(k, l, TileType<DarkBrick>());
                            tile.slope(1);
                            break;
                    }

                    switch (_shadowhouseWall[y, x])
                    {
                        case 1:
                            tile.wall = (ushort)WallType<DarkBrickWall>();
                            break;
                    }
                }
            }
            for (int y = 0; y < _shadowhouseFurniture.GetLength(0); y++)
            {
                for (int x = 0; x < _shadowhouseFurniture.GetLength(1); x++)
                {
                    int k = i + x;
                    int l = j + y;
                    switch (_shadowhouseFurniture[y, x])
                    {
                        case 2:
                            WorldGen.PlaceDresserDirect(k, l, (ushort)TileType<Crate>(), 0, -1);
                            break;
                        case 3:
                            WorldGen.Place4x2(k, l, (ushort)TileType<ShadowAltar>(), -1, 0);
                            shadowAltarCoordsX = k + 1;
                            shadowAltarCoordsY = l;
                            break;
                        case 4:
                            WorldGen.PlaceObject(k, l, (ushort)TileType<DarkbrickDoor.DarkbrickDoorClosed>());
                            break;
                    }
                }
            }
            //Protect from other generation
            WorldGen.structures.AddStructure(new Rectangle(i, j, _shadowhouseShape.GetLength(1), _shadowhouseShape.GetLength(0)), 50);
        }

