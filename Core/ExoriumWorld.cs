        public override TagCompound Save()
        {
            var downed = new List<string>();
            if (downedShadowmancer)
                downed.Add("shadowmancer");
            if (downedBlightslime)
                downed.Add("blightslime");

            return new TagCompound
            {
                ["downed"] = downed,
                ["shadowAltarCoordsX"] = shadowAltarCoordsX,
                ["shadowAltarCoordsY"] = shadowAltarCoordsY
            };
        }

        public override void Load(TagCompound tag)
        {
            shadowAltarCoordsX = (int)tag.Get<int>("shadowAltarCoordsX");
            shadowAltarCoordsY = (int)tag.Get<int>("shadowAltarCoordsY");

            var downed = tag.GetList<string>("downed");
            downedShadowmancer = downed.Contains("shadowmancer");
            downedBlightslime = downed.Contains("blightslime");
        }

        public override void LoadLegacy(BinaryReader reader)
        {
            int loadVersion = reader.ReadInt32();
            if (loadVersion == 0)
            {
                shadowAltarCoordsX = reader.ReadInt32();
                shadowAltarCoordsY = reader.ReadInt32();
                BitsByte flags = reader.ReadByte();
                downedShadowmancer = flags[0];
                downedBlightslime = flags[1];
            }
            else
            {
                mod.Logger.Error("ExoriumMod: Unknown loadVersion:" + loadVersion);
            }
        }

        public override void NetSend(BinaryWriter writer)
        {
            writer.Write(shadowAltarCoordsX);
            writer.Write(shadowAltarCoordsY);
            BitsByte bosses1 = new BitsByte(downedShadowmancer, downedBlightslime);
            writer.Write(bosses1);
        }

        public override void NetReceive(BinaryReader reader)
        {
            shadowAltarCoordsX = reader.ReadInt32();
            shadowAltarCoordsY = reader.ReadInt32();
            BitsByte bosses1 = reader.ReadByte();
            downedShadowmancer = bosses1[0];
            downedBlightslime = bosses1[1];
        }

        public override void Initialize()
        {
            downedShadowmancer = false;
            downedBlightslime = false;
        }

        public override void ModifyWorldGenTasks(List<GenPass> tasks, ref float totalWright)
        {
            int ShiniesIndex = tasks.FindIndex(genpass => genpass.Name.Equals("Shinies"));
            if (ShiniesIndex != -1)
            {
                tasks.Insert(ShiniesIndex + 1, new PassLegacy("Exorium Mod Ores", ExoriumOreGeneration));
            }

            int DirtRockWallRunnerIndex = tasks.FindIndex(genpass => genpass.Name.Equals("Dirt Rock Wall Runner"));
            if (DirtRockWallRunnerIndex != -1)
            {
                tasks.Insert(DirtRockWallRunnerIndex + 1, new PassLegacy("Deadlands", delegate (GenerationProgress progress)
                  {
                      progress.Message = "Generating Deadlands";
                      DeadlandsGeneration();
                  }));
            }
        }

        private void ExoriumOreGeneration(GenerationProgress progress)
        {
            progress.Message = "Generating Exorium Ores";
            //BlightSteel spawn
            for (int i = 0; i < (int)((double)(Main.maxTilesX * Main.maxTilesY) * 12E-05); i++)
            {
                int x = WorldGen.genRand.Next(0, Main.maxTilesX);
                int y = WorldGen.genRand.Next((int)WorldGen.rockLayer, Main.maxTilesY); //Spawn in cavern layer
                Tile tile = Framing.GetTileSafely(x, y);
                if (tile.active() && tile.type == TileID.Stone)
                {
                    WorldGen.TileRunner(x, y, (double)WorldGen.genRand.Next(2, 3), WorldGen.genRand.Next(1, 3), TileType<BlightedOre>(), false, 0f, 0f, false, true);
                }
            }
            //RimeStone spawn
            for (int i = 0; i < (int)((double)(Main.maxTilesX * Main.maxTilesY) * 5E-04); i++)
            {
                int x = WorldGen.genRand.Next(0, Main.maxTilesX);
                int y = WorldGen.genRand.Next((int)WorldGen.worldSurfaceLow, Main.maxTilesY);
                Tile tile = Framing.GetTileSafely(x, y);
                if (tile.active() && tile.type == TileID.IceBlock || tile.type == TileID.SnowBlock)
                {
                    WorldGen.TileRunner(x, y, (double)WorldGen.genRand.Next(6, 7), WorldGen.genRand.Next(3, 5), TileType<RimeStone>(), false, 0f, 0f, false, true);
                }
            }
            //DuneStone spawn
            for (int i = 0; i < (int)((double)(Main.maxTilesX * Main.maxTilesY) * 6E-04); i++)
            {
                int x = WorldGen.genRand.Next(0, Main.maxTilesX);
                int y = WorldGen.genRand.Next((int)WorldGen.worldSurfaceLow, Main.maxTilesY);
                Tile tile = Framing.GetTileSafely(x, y);
                if (tile.active() && (tile.type == TileID.Sandstone || tile.type == TileID.Sand || tile.type == TileID.HardenedSand))
                {
                    WorldGen.TileRunner(x, y, (double)WorldGen.genRand.Next(6, 7), WorldGen.genRand.Next(3, 5), TileType<DuneStone>(), false, 0f, 0f, false, true);
                }
            }
        }

        private void DeadlandsGeneration()
        {
            bool success = false;
            int attempts = 0;
            while(!success)
            {
                attempts++;
                int width = 200;
                int i = WorldGen.genRand.Next((Main.maxTilesX / 8) + 10, Main.maxTilesX - (Main.maxTilesX / 8) - width - 10);
                int k = i + width;
                if ((i <= Main.maxTilesX / 2 - (Main.maxTilesX / 8) || i >= Main.maxTilesX / 2 + (Main.maxTilesX / 8)) && (k <= Main.maxTilesX / 2 - (Main.maxTilesX / 8) || k >= Main.maxTilesX / 2 + (Main.maxTilesX / 8))) //not within center 5th of world or outer 10ths(ocean)
                {
                    int j = 0;
                    int l = 0;
                    while (!Framing.GetTileSafely(i, j).active() && (double)j < Main.worldSurface)
                    {
                        j++;
                    }
                    while (!Framing.GetTileSafely(k, l).active() && (double)l < Main.worldSurface)
                    {
                        l++;
                    }
                    if(((Framing.GetTileSafely(k, l).type == TileID.Dirt) && (Framing.GetTileSafely(k, l).type == TileID.Dirt)) || attempts > 100000)//if too many attempts dirt is no longer required
                    {
                        j += 50;
                        l += 50;
                        bool placementOK = true;
                        for (int m = i - Main.maxTilesX / 60; m <= k + Main.maxTilesX / 60; m++)
                        {
                            for (int n = Math.Min(j,l) - Main.maxTilesX / 30; n <= Math.Max(j,l) + Main.maxTilesX / 30; n++)
                            {
                                if (Framing.GetTileSafely(m, n).active())
                                {
                                    if (!WorldGen.InWorld(m, n, 50))
                                    {
                                        placementOK = false;
                                        break;
                                    }
                                    int type = Framing.GetTileSafely(m, n).type;
                                    if (type == TileID.BlueDungeonBrick || type == TileID.GreenDungeonBrick || type == TileID.PinkDungeonBrick || type == TileID.Cloud || type == TileID.RainCloud || type == TileID.WoodBlock || type == TileID.LivingWood || type == TileID.Ebonstone || type == TileID.Crimstone || type == TileID.SandstoneBrick)
                                    {
                                        placementOK = false;
                                        break;
                                    }
                                }
                            }
                        }
                        if (placementOK || attempts > 100000000) //if far too many attempts, spawn anyway (this may disrupt other structures but hopefully won't happen too often)
                        {
                            success = SetDeadlands(i, j, k, l);
                        }
                    }
                }
            }
        }

        private bool SetDeadlands (int i, int j, int k, int l)
        {
            float sizeScale = Main.maxTilesX / 14f;  
            if (j < 150 || l < 150)
                return false;
            for (int m = i - Main.maxTilesX / 20; m <= k + Main.maxTilesX / 20; m++)
            {
                for (int n = Math.Min(j, l) - Main.maxTilesX / 20; n <= Math.Max(j, l) + Main.maxTilesX / 20; n++)
                {
                    if (sizeScale >= (Math.Sqrt(Math.Pow(i - m, 2) + Math.Pow(j - n, 2))) + (Math.Sqrt(Math.Pow(k - m, 2) + Math.Pow(l - n, 2))))
                    {
                        if (WorldGen.InWorld(m, n, 30)) //Don't think this is actually necessary
                        {
                            switch (Framing.GetTileSafely(m, n).type)
                            {
                                case TileID.Copper:
                                case TileID.Tin:
                                case TileID.Lead:
                                case TileID.Iron:
                                case TileID.Silver:
                                case TileID.Tungsten:
                                case TileID.Gold:
                                case TileID.Platinum:
                                case TileID.Crimtane:
                                case TileID.Demonite:
                                    Framing.GetTileSafely(m, n).type = (ushort)TileType<BlightedOre>();
                                    break;
                                case TileID.BlueDungeonBrick:
                                case TileID.GreenDungeonBrick:
                                case TileID.PinkDungeonBrick:
                                case TileID.LivingWood:
                                    break;
                                default:
                                    Framing.GetTileSafely(m, n).type = (ushort)TileType<AshenDust>();
                                    break;
                            }
                            if (Framing.GetTileSafely(m, n).wall != 0 && Framing.GetTileSafely(m, n).wall != 7 && Framing.GetTileSafely(m, n).wall != 8 && Framing.GetTileSafely(m, n).wall != 9 && 
                                Framing.GetTileSafely(m, n).wall != 244 && Framing.GetTileSafely(m, n).wall != 94 && Framing.GetTileSafely(m, n).wall != 95 && Framing.GetTileSafely(m, n).wall != 96 && 
                                Framing.GetTileSafely(m, n).wall != 97 && Framing.GetTileSafely(m, n).wall != 98 && Framing.GetTileSafely(m, n).wall != 99 && Framing.GetTileSafely(m, n).wall != 100 && 
                                Framing.GetTileSafely(m, n).wall != 101 && Framing.GetTileSafely(m, n).wall != 102 && Framing.GetTileSafely(m, n).wall != 103 && Framing.GetTileSafely(m, n).wall != 104 && 
                                Framing.GetTileSafely(m, n).wall != 105)
                                Framing.GetTileSafely(m, n).wall = (ushort)WallType<AshenDustWall>();
                        }
                    }
                }
            }
            return true;
        }

